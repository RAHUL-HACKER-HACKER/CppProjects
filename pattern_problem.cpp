using namespace std;
#include <iostream>
 main(){
 	
 	int n;
 	cout<<"inter num of pattern:";
 	cin>>n;
 	
 	for(int i=0;i<n;i++){
 		
 		for(int j=0;j<=i;j++){
 			cout<<"*";
 			
		 }
		 cout<<endl;
 		
	 }
	 cout<<endl;
 	for(int i=n;i>0;i--){
 		
 		for(int j=0;j<=i;j++){
 			cout<<j+1;
 			
		 }
		 cout<<endl;
 }

 }
